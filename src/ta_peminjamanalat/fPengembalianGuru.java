    public void tampil_masuk(){
         masuk = (DefaultTableModel) tbMasuk.getModel();
        try {
            konek.st = konek.conn.createStatement();
            String query = "SELECT nama, mapel, nama_barang, jumlah_barang, DATE_FORMAT(tgl_pinjam, '%d/%m/%Y') AS tanggal_pinjam, "
                    + "DATE_FORMAT(tgl_kembali, '%d/%m/%Y') AS tanggal_kembali "
                    + "FROM tb_peminjamanguru WHERE tgl_kembali ORDER BY tgl_pinjam DESC";
            
            konek.rs = konek.st.executeQuery(query);
            while (konek.rs.next()) {
                
                String nama = konek.rs.getString("nama");
                String mapel = konek.rs.getString("mapel");
                String nama_barang = konek.rs.getString("nama_barang");
                String jumlah_barang = konek.rs.getString("jumlah_barang");
                String tgl_pinjam = konek.rs.getString("tanggal_pinjam");
                String tgl_kembali = konek.rs.getString("tanggal_kembali");
                
                
                String [] data = {nama, mapel, nama_barang, jumlah_barang, tgl_pinjam, tgl_kembali};
                masuk.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal ambil data"+e);
        }
        tbMasuk.setModel(masuk);
    }

       
