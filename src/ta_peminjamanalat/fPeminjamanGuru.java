    public void resetAll(){
        
        txtNip.setText("");
        txtNamabrg.setText("");
        txtNama.setText("");
        txtStok.setText("");
        txtJumlahbrg.setText("");
        cboMapel.setSelectedIndex(0);
        
        masuk = (DefaultTableModel) tbMasuk.getModel();
        keluar = (DefaultTableModel) tbKeluar.getModel();
       
        int rowCount1 = masuk.getRowCount();
        int rowCount2 = keluar.getRowCount();
        //Remove rows one by one from the end of the table
        
        for (int i = rowCount1 - 1; i >= 0; i--) {
            masuk.removeRow(i);
        }
        for (int i = rowCount2 - 1; i >= 0; i--) {
            keluar.removeRow(i);
        }
    }
    
    public void tampil_masuk(){
        
        masuk = (DefaultTableModel) tbMasuk.getModel();
        
        try {
            konek.st = konek.conn.createStatement();
            String query = "SELECT nip, nama_barang, nama, jumlah_barang, mapel, "
                    + "DATE_FORMAT(tgl_pinjam, '%d/%m/%Y') AS tanggal_pinjam FROM tb_peminjamanguru";
            konek.rs = konek.st.executeQuery(query);
            while (konek.rs.next()) {
                
                String nip = konek.rs.getString("nip");
                String nama_barang = konek.rs.getString("nama_barang");
                String nama = konek.rs.getString("nama");
                String jumlah_barang = konek.rs.getString("jumlah_barang");
                String mapel = konek.rs.getString("mapel");
                String tgl_pinjam = konek.rs.getString("tanggal_pinjam");
                
                
                String [] data = {nip, nama_barang, nama, jumlah_barang, mapel, tgl_pinjam};
                masuk.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal ambil data"+e);
        }
        tbMasuk.setModel(masuk);
    }

       
    public void tampil_keluar(){
        
     keluar = (DefaultTableModel) tbKeluar.getModel();
     
        try {
            konek.st = konek.conn.createStatement();
            String query = "SELECT nip, nama_barang, nama, jumlah_barang, mapel, DATE_FORMAT(tgl_pinjam, '%d/%m/%Y') AS tanggal_pinjam, jam_pinjam "
                    + "FROM tb_peminjamanguru WHERE jam_pinjam ORDER BY tgl_pinjam DESC";
            konek.rs = konek.st.executeQuery(query);
            while (konek.rs.next()) {
                
                String nip = konek.rs.getString("nip");
                String nama_barang = konek.rs.getString("nama_barang");
                String nama = konek.rs.getString("nama");
                String jumlah_barang = konek.rs.getString("jumlah_barang");
                String mapel = konek.rs.getString("mapel");
                String tgl_pinjam = konek.rs.getString("tanggal_pinjam");
                String jam_pinjam = konek.rs.getString("jam_pinjam");

           
                String [] data = {nama_barang, nama, jumlah_barang, mapel, tgl_pinjam, jam_pinjam};
                keluar.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal ambil data"+e);
        }
        tbKeluar.setModel(keluar);
    }
    
