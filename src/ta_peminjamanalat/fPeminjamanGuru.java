    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        // TODO add your handling code here:
        if(txtKNip.getText().isEmpty())
            JOptionPane.showMessageDialog(this, "NIP Masih Kosong");
        
        else {
            
           String sql = "SELECT nama, mapel, DATE_FORMAT(tgl_pinjam, '%d/%m/%Y') AS tanggal_pinjam, nama_barang, jumlah_barang "
                   + "FROM tb_peminjamanguru WHERE nip='"+txtKNip.getText()+"'";
           try
                
            {
                konek.st = konek.conn.createStatement();
                konek.rs = konek.st.executeQuery(sql);
            } catch (SQLException ex){
                JOptionPane.showMessageDialog(this, "Kesalahan query"+ex);
            }
             
             int size=0;
             try {
                while (konek.rs.next()){
                    size++;
           
                    txtKNama.setText(konek.rs.getString("nama").toString());
                    txtKMapel.setText(konek.rs.getString("mapel").toString());
                    txtKPinjam.setText(konek.rs.getString("tanggal_pinjam").toString());
                    txtKNamaB.setText(konek.rs.getString("nama_barang").toString());
                    txtKJumlahB.setText(konek.rs.getString("jumlah_barang").toString());
                
            }
                if(size==0)
                {
                    JOptionPane.showMessageDialog(this,"Data tidak ditemukan");
                    return;
                }
                konek.rs.close();
                konek.st.close();
            } catch (SQLException ex){
            
        }
    }//GEN-LAST:event_btnCariActionPerformed
 }
    
