    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        
        if (txtNip.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "NIP belum diisi!");
        } else if (txtNamabrg.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama Barang belum diisi!");
        } else if (txtNama.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama belum diisi!");
        } else if (txtJumlahbrg.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Jumlah Barang belum diisi!");
        } else if (txtStok.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Barang yang akan di pinjam Tidak Tersedia");
        } else if (cboMapel.getSelectedItem().equals("-Pilih Mapel-")) {
            JOptionPane.showMessageDialog(this, "Mata Pelajaran belum dipilih");
        } else {
        
            Double stok_barang = Double.valueOf(txtStok.getText());
            Double jmlh_barang = Double.valueOf(txtJumlahbrg.getText());
       
        if (stok_barang < jmlh_barang) {
            
        JOptionPane.showMessageDialog(this, "Stok Barang Tidak tersedia");
        } else {
            
            String nip, nama_barang, nama, jumlah_barang, mapel, sql, query;
            nip = txtNip.getText();
            nama_barang = txtNamabrg.getText();
            nama = txtNama.getText();
            jumlah_barang = txtJumlahbrg.getText();
            mapel = cboMapel.getSelectedItem().toString();
 
            sql = "INSERT INTO tb_peminjamanguru(nip, nama_barang,nama,jumlah_barang,mapel,tgl_pinjam)"
                  + "VALUES('"+nip+"','"+nama_barang+"','"+nama+"','"+jumlah_barang+"','"+mapel+"', CURDATE())";
            query = "UPDATE tb_peralatan SET stok_barang = stok_barang-"+jumlah_barang+" WHERE nama_barang = '"+nama_barang+"'";
        
            try {
                konek.st = konek.conn.createStatement();
                konek.st.execute(sql);
                konek.st.executeUpdate(query);
                
            }
            catch (SQLException e){
                JOptionPane.showMessageDialog(this, "Data gagal disimpan"+e);
                return;
            }
            JOptionPane.showMessageDialog(this, "Data berhasil disimpan");
        }
        
        resetAll();
        tampil_masuk();
        tampil_keluar();
       
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

