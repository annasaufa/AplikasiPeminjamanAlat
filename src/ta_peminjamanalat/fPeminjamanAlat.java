    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
  
        if (txtNis.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "NIS belum diisi!");
        } else if (txtNamabrg.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama Barang  belum diisi!");
        } else if (txtNama.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama belum diisi!");
        } else if (txtJumlahbrg.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Jumlah Barang belum diisi!");
        } else if (txtStok.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Barang yang akan di pinjam Tidak Tersedia");
        } else if (cboKelas.getSelectedItem().equals("-Pilih Kelas-")) {
            JOptionPane.showMessageDialog(this, "Kelas belum dipilih");
        } else if (cboJurusan.getSelectedItem().equals("-Pilih Jurusan-")) {
            JOptionPane.showMessageDialog(this, "Jurusan belum dipilih");
       
        } else {
        
        Double stok_barang = Double.valueOf(txtStok.getText());
        Double jmlh_barang = Double.valueOf(txtJumlahbrg.getText());
       
        if (stok_barang < jmlh_barang ) {
            
        JOptionPane.showMessageDialog(this, "Stok Barang Tidak tersedia");
        
        } else {
            
            String nis, nama_barang, nama, jumlah_barang, kelas, jurusan, sql, query;   
            nis = txtNis.getText();
            nama_barang = txtNamabrg.getText();
            nama = txtNama.getText();
            jumlah_barang = txtJumlahbrg.getText();
            kelas = cboKelas.getSelectedItem().toString();
            jurusan = cboJurusan.getSelectedItem().toString();
            
            sql   = "INSERT INTO tb_peminjaman(nis, nama_barang,nama,jumlah_barang,kelas,jurusan,tgl_pinjam)"
                    + "VALUES('"+nis+"','"+nama_barang+"','"+nama+"','"+jumlah_barang+"','"+kelas+"','"+jurusan+"', CURDATE())";
            query = "UPDATE tb_peralatan SET stok_barang = stok_barang-"+jumlah_barang+" WHERE nama_barang = '"+nama_barang+"'";
            
            try {                                                                                                                                                                                                   
                konek.st = konek.conn.createStatement();
                konek.st.execute(sql);
                konek.st.executeUpdate(query);
            }
            
            catch (SQLException e) {
                
                JOptionPane.showMessageDialog(this, "Data gagal disimpan"+e);
                return;
            }
            JOptionPane.showMessageDialog(this, "Data berhasil disimpan");
        }
        resetAll();
        tampil_masuk();
        tampil_keluar();
        
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    
