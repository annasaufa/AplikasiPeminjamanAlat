    public void resetAll(){
        
        txtNis.setText("");
        txtNamabrg.setText("");
        txtNama.setText("");
        txtStok.setText("");
        txtJumlahbrg.setText("");
        cboKelas.setSelectedIndex(0);
        cboJurusan.setSelectedIndex(0);
        
        masuk = (DefaultTableModel) tbMasuk.getModel();
        keluar = (DefaultTableModel) tbKeluar.getModel();
       
        int rowCount1 = masuk.getRowCount();
        int rowCount2 = keluar.getRowCount();
        //Remove rows one by one from the end of the table
        
        for (int i = rowCount1 - 1; i >= 0; i--) {
            masuk.removeRow(i);
        }
        for (int i = rowCount2 - 1; i >= 0; i--) {
            keluar.removeRow(i);
        }
    }
    
    public void tampil_masuk(){
        
        masuk = (DefaultTableModel) tbMasuk.getModel();
        
        try {
            konek.st = konek.conn.createStatement();
            String query = "SELECT nis, nama_barang, nama, jumlah_barang, kelas, jurusan, "
                    + "DATE_FORMAT(tgl_pinjam, '%d/%m/%Y') AS tanggal_pinjam FROM tb_peminjaman";
            konek.rs = konek.st.executeQuery(query);
            while (konek.rs.next()) {
                
                String nis = konek.rs.getString("nis");
                String nama_barang = konek.rs.getString("nama_barang");
                String nama = konek.rs.getString("nama");
                String jumlah_barang = konek.rs.getString("jumlah_barang");
                String kelas = konek.rs.getString("kelas");
                String jurusan = konek.rs.getString("jurusan");
                String tgl_pinjam = konek.rs.getString("tanggal_pinjam");
                
                
                String [] data = {nis, nama_barang, nama, jumlah_barang, kelas, jurusan, tgl_pinjam};
                masuk.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal ambil data"+e);
        }
        tbMasuk.setModel(masuk);
    }

       
