    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if (txtID.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "ID belum diisi!");
        } else if (txtNamaBrng.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama Benda belum diisi!");
        } else if (txtSpek.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Spesifikasi belum diisi!");
        } else if (txtInventaris.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "No. Inventaris belum diisi!");
        } else if (txtMAC.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "MAC / IMEI belum diisi!");
        } else if (txtStok.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Stok belum diisi!");
        } else if (txtDana.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Sumber Dana belum diisi!");
        } else if (txtTahun.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tahun Pengadaan / Perolehan belum diisi!");
        } else if (cboKategori.getSelectedItem().equals("-Pilih Kategori-")) {
            JOptionPane.showMessageDialog(this, "Kategori belum dipilih");
            
        } else {
            String id_barang, nama_barang, spek, inventaris, mac, stok, dana, tahun, kategori, sql;
            
            id_barang = txtID.getText();
            nama_barang = txtNamaBrng.getText();
            spek = txtSpek.getText();
            inventaris = txtInventaris.getText();
            mac = txtMAC.getText();
            stok = txtStok.getText();
            dana = txtDana.getText();
            tahun = txtTahun.getText();
            kategori = cboKategori.getSelectedItem().toString();
 
 sql = "INSERT INTO tb_peralatan(id_barang, nama_barang, spek, no_inventaris, mac, stok_barang, dana, tahun, kategori)"
 + "VALUES('"+id_barang+"','"+nama_barang+"','"+spek+"',"
         + "'"+inventaris+"','"+mac+"','"+stok+"','"+dana+"','"+tahun+"','"+kategori+"')";
            try {
                konek.st = konek.conn.createStatement();
                konek.st.execute(sql);  
            }
            catch (SQLException e){
                JOptionPane.showMessageDialog(this, "Data gagal disimpan"+e);
                return;
            }
            JOptionPane.showMessageDialog(this, "Data berhasil disimpan");
        }
        resetAll();
        tampil_masuk();
        tampil_keluar();
        tampil_keluar1();
    }//GEN-LAST:event_btnSimpanActionPerformed

