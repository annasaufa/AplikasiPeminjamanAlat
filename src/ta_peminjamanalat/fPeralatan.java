    public void resetAll(){
        
        txtID.setText("");
        txtNamaBrng.setText("");
        txtSpek.setText("");
        txtInventaris.setText("");
        txtMAC.setText("");
        txtStok.setText("");
        txtDana.setText("");
        txtTahun.setText("");
        cboKategori.setSelectedIndex(0);
        
        
        masuk = (DefaultTableModel) tbInput.getModel();
        keluar = (DefaultTableModel) tbStok.getModel();
        keluar1 = (DefaultTableModel) tbTahun.getModel();
       
        int rowCount1 = masuk.getRowCount();
        int rowCount2 = keluar.getRowCount();
        int rowCount3 = keluar1.getRowCount();
        
        //Remove rows one by one from the end of the table
        
        for (int i = rowCount1 - 1; i >= 0; i--) {
            masuk.removeRow(i);
        }
        for (int i = rowCount2 - 1; i >= 0; i--) {
            keluar.removeRow(i);
        }
        for (int i = rowCount3 - 1; i >= 0; i--) {
            keluar1.removeRow(i);
        }
    }
    
